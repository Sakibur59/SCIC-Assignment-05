// Helper functions
export const getJobById = (id) => {
  return jobsData.find(job => job.id === parseInt(id));
};

export const getSimilarJobs = (job) => {
  return jobsData
    .filter(j => j.id !== job.id && (j.tags.some(tag => job.tags.includes(tag)) || j.type === job.type))
    .slice(0, 3);
};